package starprograms;

public class starpro 
{
	public static void main(String[] args)
	{
		
	int i,j,h;
	{
		for(i=1;i<=5;i++)
		{
			for(j=4;j>=i;j--)
			{
				System.out.print("  ");
			}
			for(h=1;h<=i;h++)
			{
				System.out.print("* ");
			}
		}
		System.out.println();
		
		
	
		}}
}


